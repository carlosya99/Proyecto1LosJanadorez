    public boolean gHappy(String str) {
        Boolean x= true;
        for(int i=0; i<str.length(); i++){
            if(str.substring(i,i+1) .equals("g")) {
                x=false;
                if(i < str.length()-1  && str.substring(i+1,i+2) .equals("g")){
                  x=true;
                }
                if(i > 0  && str.substring(i-1,i) .equals("g")){
                  x=true;
                }
            }
        }
        return x;
    }     
